# and operator
name = "harish"
surname = "kumbhar"
while (name == "harish" and surname == "kumbhar"):
    print("Harish Kumbhar")
    break


# or operator
name = "Tejal"
surname = "kumbhar"
while (name == "Tejal" or surname == " "):
    print("Tejal Kumbhar")
    break